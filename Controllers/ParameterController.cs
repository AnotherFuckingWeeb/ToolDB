using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace ToolDB.Controllers
{
    class ParameterController
    {
        /*
         * ParameterController is the class that change parameter's values
         * -- methods --
         * -- SetParameterName: type string method that returns the name of the parameter --
         * -- SetType: type string method that return the type of the parameter (INT, VARCHAR, BIT ...etc) --
         */

        public string SetParameterName(string name)
        {
            return name;
        }

        public string SetType(string type)
        {
            return type;
        }
    }
}
